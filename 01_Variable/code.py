# Variable is a memore location to store values (or in other words variable can be a name for any given value)
# 1. Variable name should start with letter or underscore (can also have numbers but not at begining)
# 2. In Variable name no special character are allowed(except _)
# 3. Variables are case sensitive
# 4. Variable name should not have a Python keyword.

#Examples:-

#Numbers & Floats
x = 1500 # if we want to write 1500 anywhere in code then can write only x instead of 1500
price = 9.99
discount = 0.2

result = price * (1-discount)
print(result)

# Strings
name = "Rahul"
print(name)
print(name *2)

#Changing Variable Values
a = 25
b = a

print(a)
print(b)


b = 17

print(a)
print(b)

